package it.polito.tdp.borders;

public class Adiacenza {

	int stato1;
	int stato2;
	
	public int getStato1() {
		return stato1;
	}
	public void setStato1(int stato1) {
		this.stato1 = stato1;
	}
	public int getStato2() {
		return stato2;
	}
	public void setStato2(int stato2) {
		this.stato2 = stato2;
	}
	public Adiacenza(int stato1, int stato2) {
		super();
		this.stato1 = stato1;
		this.stato2 = stato2;
	}
	
}
